var genres = [];
function selectedGenres(class_name)
{

   var existing = false;
   var genre = document.querySelector(`.${class_name}`);

   console.log("genres.length: " + genres.length);
   console.log("existing: " + existing);
   console.log("class_name: " + class_name);

   for (let i = 0; i < genres.length; i++)
   {
      if (genres[i] === genre.value)
      {
         existing = true;

         if (i === 0)
         {
            genres.shift();
         }
         else
         {
            genres.splice(i, i);
         }
      }
   }

   if (!(existing))
   {
      genres.push(genre.value);
   }

   console.log("existing: " + existing);

   console.log("genres: " + genres);
}


