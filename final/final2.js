var counterG = 0;
var titlesG = [];
var tempTitleG = [];
function titlesCWGenres()
{
   if (!(counterG === 0))
   {
      tempTitleG = titlesG;
      titlesG = [];
   }

   for (let i = 0; i < genres.length; i++) //selected genre icinde geziyo
   {
      for (let j = 0; j < movies.length; j++) //movies icinde geziyo geziniyo
      {
         for (let k = 0; k < movies[j].genre.length; k++) //movie icindeki genre da geziyo
         {
            if (genres[i] === movies[j].genre[k] && !(titlesG.includes(movies[j].title)))
            {
               titlesG.push(movies[j].title);
            }
         }
      }
   }
   counterG++;
   console.log("Genre selection'undan sonra titlesG: " + titlesG);
}


